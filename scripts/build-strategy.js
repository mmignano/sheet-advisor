#!/usr/bin/env node
/**
 * build-strategy.js — Generate src/StrategyConfig.js from a strategy markdown file.
 *
 * Usage:
 *   node scripts/build-strategy.js [path-to-strategy.md]
 *
 * Defaults to strategies/strategy-example.md if no path is given.
 * After running, do `clasp push` to deploy the updated strategy.
 */

const fs = require('fs');
const path = require('path');

const mdPath = process.argv[2] || 'strategies/strategy-example.md';
const resolvedPath = path.resolve(mdPath);

if (!fs.existsSync(resolvedPath)) {
  console.error('Error: Strategy file not found: ' + resolvedPath);
  process.exit(1);
}

const content = fs.readFileSync(resolvedPath, 'utf-8');
const lines = content.split('\n');

// Escape each line for embedding in a JS single-quoted string
const jsLines = lines.map(function (line) {
  const escaped = line.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
  return "  '" + escaped + "'";
});

const output =
  '/**\n' +
  ' * StrategyConfig.js — Strategy definition (auto-generated)\n' +
  ' *\n' +
  ' * Source: ' + path.basename(mdPath) + '\n' +
  ' * Generated: ' + new Date().toISOString().split('T')[0] + '\n' +
  ' *\n' +
  ' * DO NOT EDIT THIS FILE DIRECTLY.\n' +
  ' * Edit the source markdown file, then run:\n' +
  ' *   npm run build-strategy\n' +
  ' *   (or: node scripts/build-strategy.js path/to/strategy.md)\n' +
  ' */\n' +
  '\n' +
  'var STRATEGY_MARKDOWN = [\n' +
  jsLines.join(',\n') +
  '\n].join(\'\\n\');\n';

const outPath = path.resolve('src/StrategyConfig.js');
fs.writeFileSync(outPath, output);
console.log('Generated src/StrategyConfig.js from ' + mdPath);
